package e_oop;

public class Calculator {

	double plus (double x, double y){
		return x + y;
	}
	
	double difference (double x, double y){
		return x - y;
	}
	
	double multiplication (double x, double y){
		return x * y;
	}
	
	double division (double x, double y){
		return x / y;
	}
	
	double remainder (double x, double y){
		return x % y;
	}
	
	
}
